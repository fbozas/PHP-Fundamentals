<?php
    //------------------and-----------------------------

    //true && true
    echo "true and true makes " . (true && true) . "\n";
    //true && false
    echo "true and false makes ". (true && false) . "\n";
    //false && true
    echo "false and true makes ". (false && true) . "\n";
    //false && false
    echo "false and false makes ". (false && false) . "\n";

    //-------------------or----------------------------

    //true || true
    echo "true or true makes ". (true || true) . "\n";
    //true || false
    echo "true or false makes ". (true || false) . "\n";
    //false || true
    echo "false or true makes ". (false || true) . "\n";
    //false || false
    echo "false or false makes ". (false || false) . "\n";

    //-------------------!----------------------------

    //! true
    echo "not true makes ". (!true) . "\n";
    //! false
    echo "not false makes ". (!false) . "\n";
